fun main(){
    var vokal = 'Z'

    println("Huruf "+vokal--) //Z
    println("Huruf "+vokal--) //Y
    println("Huruf "+vokal--) //X
    vokal=vokal-4
    println("Huruf "+vokal--) //S
    println("Huruf "+vokal++) //R
    println("Huruf "+vokal++) //s
    vokal=vokal+4
    println("Huruf "+vokal++) //X
    println("Huruf "+vokal++) //Y
    println("Huruf "+vokal++) //Z
}
